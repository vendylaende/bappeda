                <p>Untuk mewujudkan Visi dan melaksanakan Misi di atas, Kementerian PPN/Bappenas menetapkan 3 (tiga) tujuan yang akan dicapai dalam 5 (lima) tahun ke depan, sebagai berikut:</p>

                <p><strong>Misi-1&nbsp;</strong></p>

                <p><strong>Tujuan-1 :</strong> Mewujudkan perencanaan pembangunan yang berorientasi hasil dan mempercepat kemajuan Indonesia.</p>

